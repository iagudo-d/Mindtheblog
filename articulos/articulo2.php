						<div class="titular-articulo">
							<h1 itemprop="headline">Experiencia negativa con un vendedor de Ebay</h1>
						</div>
						<div class="datos-creacion-articulo">
							<span class="tema-articulo" itemprop="about">Comercio Electronico</span>
							<span class="fecha-articulo" itemprop="datePublished">07-07-2015</span>
							<span class="autor-articulo" itemprop="author" itemscope itemtype="http://schema.org/Person"><span itemprop="name">Iván Agudo Díaz</span></span>
						</div>
						<img src="" alt="" itemprop="image"/>
						<p>Bueno, he decidido contaros, tras unas cuantas experiencias, buenas experiencias de compra en la de sobra conocida página de subastas, (aunque últimamente mas de venta directa) Ebay, mi primera experiencia negativa con un vendedor. Así como dar a conocer una mala práctica que Ebay no erradica, lo que hace que los compradores en algunas ocasiones recibamos un voto negativo inmerecido ( Mi primer voto negativo!!! ).</p>
						<p>La historia comienza como… bueno, como cabría esperar, con un cliente (ese soy yo) en busca de un artículo a través de la Red, os diré que para entender parte del trasfondo de mi queja, hay que entender las circunstancias por las cuales me decido a comprar este artículo a este vendedor y en dicho portal, como algunos de vosotros sabréis soy una persona inquieta y amante del DIY y las nuevas tecnologías, y como tal, he querido emprender un nuevo proyecto de domótica casera, que trataré de exponer en el blog en el futuro a medida que este avance, pues bien, dicho proyecto basa su funcionamiento experimental, Ojo!! Experimental por mi parte, ya sabemos todos, que hay videos y tutoriales a patadas en muchos portales dedicados a ello, en la archiconocida placa Arduino™, tenía la esperanza de que dicha placa llegara en un tiempo no demasiado elevado, pero tampoco necesitaba que fuera un envío urgente y puesto que solo era para hacer unas pruebas, decidí empezar mi andanza con un producto no oficial, cuando digo no oficial me refiero a una copia legal, esto para los que no conozcáis el mundo de Arduino™ se puede hacer, siempre y cuando no se haga uso de su logotipo ni su nombre comercial sin avisar de que es un producto derivado, si quieres conocer mas  acerca de las posibilidades que te ofrecen para distribuir copias de este producto de forma legal o informarte de este tema puedes visitar la <a href="https://www.arduino.cc/en/Trademark/HomePage?from=Main.Trademark." target="_blank" class="imagenRedesSocialesMenu"/>página de licencia comercial de Arduino.cc</a></p>
						<p>Dicho todo esto, os comento lo ocurrido para evitar estas prácticas por parte de ciertos vendedores poco confiables, ya que en eBay, la posibilidad de hacer un comentario para avisar a otros compradores es extremadamente reducida. Tras buscar en varios sitios web, en busca de un precio aceptable en un plazo de entrega aceptable, y no menos importante a un vendedor nacional, hay que decir que este producto lo puedes comprar a vendedores asiáticos un 50% más barato con un plazo de entrega superior, el día 11 de Mayo de 2015 realizo el pedido en la anteriormente citada página de compras, haciendo el pago efectivo mediante la plataforma de pago PayPal de forma inmediata, en este momento se puede decir que concluyen todas mis obligaciones como comprador para formalizar dicha compra. Como muchos sabemos en esta plataforma el vendedor vota al comprador por la corrección en su transacción, la cual ya estaba concluida y de forma correcta. El comprador ha de votar al vendedor por la corrección en el envío (que este se haya efectuado y de forma correcta), producto (que dicho producto se ajuste a lo publicado), comunicación en caso de que alguna de las partes lo necesite, por lo que los tramites puntuables por parte del comprador en la mayoría de las ocasiones finalizan antes que las del Vendedor, es por esto que en mi opinión el vendedor debería de puntuar al comprador con anterioridad, no como habitualmente se hace y eBay permite, que el vendedor espere a ser votado, para tomar represalias ante un voto negativo sea cierto o no.</p>
						<p>Ahora bien, espero al primer día del plazo de entrega y nada, al segundo… y nada. Al tercero y último… y nada, como el envío es certificado y con número de seguimiento trato de acceder a la página del localizador de envíos de correos a través de su página principal y… Opppps!! Por labores de mantenimiento vuelva a intentarlo más tarde, espero 5 o 6 horas y lo mismo, al suponer que no todo el portal está en estado de mantenimiento, busco la puerta trasera, o mejor dicho la puerta directa para acceder al localizador de envíos y ahí está, la puerta abierta, accedo al localizador de envíos y meto el código, solo dos entradas en la base de datos y poco esclarecedoras,  una en la que dice que el envío ha sido aceptado el día 11 y otra que se encuentra en tránsito desde el día 13, ¿Qué será en tránsito?, ¿Cuánto durará el tránsito?, lo dejo estar y espero a la semana siguiente sabedor de que ya siendo viernes no lo recibiré, pasan los días y me pongo en contacto con el vendedor para comentarle que aún no he recibido el envío y noto que el solo pretende justificar que él lo ha entregado y que poco puede hacer, tras varios correos intercambiados a través de la plataforma de eBay (Que es el método que se debe usar y contándonos el uno al otro nuestras discrepancias,  yo le comento que el responsable de los anuncios y de las fechas de entrega es el Vendedor, a lo que él me responde que hay una clausula en la que el declina toda responsabilidad al operador logístico, bien metidos en temas de si yo he puesto y solo me responsabilizo a medias decido dejar las discusiones. Pasados unos días el opta por ponerse en contacto conmigo externamente a la plataforma mediante los cuales yo no puedo reclamar.</p>
						<img class="article-img" src="../img/articulo2-experiencia-negativa-vendedor-ebay/localizador-envios.png" alt="Imagen con los datos del localizador de envios de Correos"/>
						<p>El día 21 veo que la base de datos de correos se ha actualizado, y que ese día mi paquete se encuentra en proceso de entrega, ¡Genial! Hoy lo recibo, que iluso, tras esperar toda la mañana en mi casa… Nada!. El día 22 será, el día 22 el vendedor se pone en contacto conmigo a las 14:00 y me dice que le han notificado que se ha intentado efectuar la entrega y que no había sido posible, lejos de la veracidad puesto que yo había estado en mi domicilio toda la mañana y nadie intento entregarlo, a las 14:30 me presento en correos y tras poner la pertinente reclamación les pido que no traten de entregar el paquete más veces, que lo dejen en la oficina que yo pasaré a recogerlo. Finalmente a las 16:20 regreso a la oficina de correos y recojo el envío.</p>
						<p>En cuanto abro el sobre de correos y el sobre antiestático en el que venía la placa, me encuentro con que el proceso de fabricación ha sido de lo más chapucero posible ( estaba de sobra convencido de que el producto no tendría la calidad del original, pero no esta copia chapucera, de todo se aprende ), los zócalos de los conectores torcidos ( es como si compras una plancha y el enchufe está torcido y a duras penas, puedes insertarlo en las bases de enchufe de tu casa ), la placa llena de sustancia para favorecer la soldadura, no obstante la soldadura esta echa por un aficionado, sin brillo alguno, lo que contrasta con la cantidad de colofonia que la habían aplicado, y lo más flagrante de todo el uso que hace la placa de los logotipos y emblemas de la marca, cuando solo es una copia, hecho que en su anuncio expone claramente el vendedor, de esto individuo, si has de hacerte responsable y aquí no hay letra pequeña.</p>
						<p>Tras un periodo probando la placa decido dar mi voto, negativo por supuesto. Así como declarar en la votación de que el producto es una copia que hace uso de un logotipo de marca de forma ilegal, el día 24 de Junio pone un comentario negativo en respuesta al mío, en el que me declara como comprador problemático desde el primer momento y que no soy recomendable, como muchos vendedores, tomando represalias cuando un consumidor se defiende de las malas prácticas de los vendedores, así que documentando el hecho, he decidido ponerlo en conocimiento del fabricante y propietario de la marca, para que tome las medidas oportunas contra este caradura que se beneficia a costa de los demás.</p>
